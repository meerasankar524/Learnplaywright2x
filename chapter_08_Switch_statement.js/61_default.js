let day = 10;
//1 - mon, 2 - tues(

switch (day) {
    case 1:
        console.log("Mon");
        break;
    case 2:
        console.log("Tues");
        break;
    case 3:
        console.log("Wed");
        break;
    case 4:
        console.log("Thur");
        break;
    case 5:
        console.log("Fri");
        break;
    case 6:
        console.log("Sat");
        break;
    case 7:
        console.log("Sun");
        break;
    default:
        console.log("Only 7 days");

}


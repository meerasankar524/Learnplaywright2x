let day = 7;
//1 - mon, 2 - tues(

switch (day) {
    case 1:
        console.log("Mon");

    case 2:
        console.log("Tues");

    case 3:
        console.log("Wed");

    case 4:
        console.log("Thur");

    case 5:
        console.log("Fri");

    case 6:
        console.log("Sat");

    case 7:
        console.log("Sun");

    default:
        console.log("Only 7 days");

}

//multi statement matcher
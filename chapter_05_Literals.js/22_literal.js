let n = 25; // this is a numeric literal assigned to a variable named age.
console.log(typeof n); // "number"
let age = "meera"; // this is a string literal assigned to a variable named age. 

let isStudent = true; // this is a boolean literal assigned to a variable named isStudent.

let pi = 3.14; // this is a numeric literal assigned to a variable named pi.

let name = "John"; // this is a string literal assigned to a variable named name.

let nullValue = null; // this is a null literal assigned to a variable named nullValue.

let undefinedValue; // This variable is declared but not assigned, so it has the value undefined.


//typeof operator to check the type of literals
console.log(typeof age); // string

//tell you the type of literal
console.log(typeof isStudent); // boolean
console.log(typeof pi); // number
console.log(typeof name); // string
console.log(typeof nullValue); // object (this is a quirk in JavaScript, null is considered an object)
console.log(typeof undefinedValue); // undefined

// single and double quotes for string literals

let single = 'Hello, World!';
let withDouble = "Hello, World!";

console.log(single);;
console.log(withDouble);

console.log(typeof single);
console.log(typeof withDouble);

let oneletter = 'A'; // Still a string, not a character type
console.log(oneletter);
console.log(typeof oneletter);
